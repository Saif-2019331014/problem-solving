using namespace std;

typedef long long              ll;
typedef double                 dbl;
typedef unsigned long long     ull;

void solve()
{
    ll n;
    cin>>n;

    if(n==0 || n==1 || n==2)
    {
        cout<<0<<SS<<0<<SS<<n<<endl;
        return;
    }

    vll v = {0, 1};
    ll sz = 2;

    while(1)
    {
        ll cur = v[sz-1]+v[sz-2];
        v.pb(cur);
        sz++;

        if(cur>=n)
            break;
    }

    for(ll i=sz-1; i>=0; i--)
    {
        if(v[i]==n)
        {
            cout<<v[i-4]<<SS<<v[i-3]<<SS<<v[i-1]<<endl;
            return;
        }
    }
}

