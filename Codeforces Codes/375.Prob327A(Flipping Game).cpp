void solve()
{
    int n, ans=0;
    cin>>n;
    vi v(n+2);

    for(int i=1; i<=n; i++)
    {
        cin>>v[i];
        v[i] += v[i-1];
    }

    for(int i=n; i>=1; i--)
    {
        for(int j=1; j<=i; j++)
        {
            int cur, pans;
            cur = v[i]-v[j-1];
            pans = (v[n]-cur)+((i-j+1)-cur);
            ans = max(ans, pans);
        }
    }

    cout<<ans<<endl;
}

