void solve()
{
    int n, ans=0;
    string s;
    cin>>s;
    n = s.size();
    bool check[n+5];
    memset(check, 0, sizeof(check));

    for(int i=1; i<n; i++)
    {
        if(i>1 && !check[i-2] && s[i]==s[i-2])
            check[i] = 1;

        if(!check[i-1] && s[i]==s[i-1])
            check[i] = 1;

        ans += check[i];
    }

    cout<<ans<<endl;
}

