typedef long long              ll;
typedef double                 dbl;
typedef unsigned long long     ull;

void solve()
{
    ll n, k;
    cin>>n>>k;
    string s = string(n, 'a');

    for(ll i=1, j=1; i<n; i++, j++)
    {
        if(k<=i)
        {
            s[i] = 'b';
            s[k-1] = 'b';
            break;
        }

        k -= j;
    }

    reverse(s.begin(), s.end());
    cout<<s<<endl;
}

