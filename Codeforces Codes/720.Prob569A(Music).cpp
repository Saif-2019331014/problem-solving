void solve()
{
    int T, S, q;
    cin>>T>>S>>q;
    int ans = 0;

    while(S < T)
    {
        int l = 1;
        int r = 1000000; /// must be bigger than T
        int m;

        while(l+1<r)
        {
            m = l+((r-l)>>1);

            if((m*q) <= ((q-1)*m)+S)
                l = m;
            else
                r = m;
        }

        S += (q-1)*l;
        ans++;
    }

    cout<<ans<<endl;
}

