bool isunlockable(int i, int sum)
{
    if(i==0)
    {
        if(sum%360==0)
            return 1;

        return 0;
    }

    return (isunlockable(i-1, sum+v[i])|isunlockable(i-1, sum-v[i]));
}

void solve()
{
    int n;
    cin>>n;
    v.assign(n+2, 0);

    for(int i=1; i<=n; i++)
        cin>>v[i];

    if(isunlockable(n, 0))
        cout<<"YES"<<endl;
    else
        cout<<"NO"<<endl;
}

