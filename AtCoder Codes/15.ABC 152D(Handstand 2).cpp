void solve()
{
    ll n, ans=0, x, y, p=1, arr[10][10]={0};
    cin>>n;

    for(ll i=1; i<=n; i++)
    {
        x = i/p;

        if(x>9)
        {
            x/=10;
            p *= 10;
        }

        y = i%10;
        arr[x][y]++;
    }

    for(ll i=1; i<=9; i++)
        for(ll j=1; j<=9; j++)
            ans += arr[i][j]*arr[j][i];

    cout<<ans<<endl;
}

